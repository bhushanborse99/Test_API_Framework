package Pojo;

public class DestinationRoute {
	
	private String routeSCAC;
	private String sequenceNumber;
	private String r260JunctionId;
	private String destinationStationNumber;
	private String destinationStateCode;
	
	public String getRouteSCAC() {
		return routeSCAC;
	}
	public void setRouteSCAC(String routeSCAC) {
		this.routeSCAC = routeSCAC;
	}
	public String getSequenceNumber() {
		return sequenceNumber;
	}
	public void setSequenceNumber(String sequenceNumber) {
		this.sequenceNumber = sequenceNumber;
	}
	public String getR260JunctionId() {
		return r260JunctionId;
	}
	public void setR260JunctionId(String r260JunctionId) {
		this.r260JunctionId = r260JunctionId;
	}
	public String getDestinationStationNumber() {
		return destinationStationNumber;
	}
	public void setDestinationStationNumber(String destinationStationNumber) {
		this.destinationStationNumber = destinationStationNumber;
	}
	public String getDestinationStateCode() {
		return destinationStateCode;
	}
	public void setDestinationStateCode(String destinationStateCode) {
		this.destinationStateCode = destinationStateCode;
	}


}
